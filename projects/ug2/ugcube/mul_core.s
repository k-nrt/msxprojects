MulCore_AxDE_HL:
    ld      h,#0
    ld      l,#0

MulCore_AxDE_HL_Loop:
	or		a
	ret		z

	srl		a	
	jr		nc,MulCore_AxDE_HL_Next

	add		hl,de

MulCore_AxDE_HL_Next:
	sla		e
	rl		d
	jr		MulCore_AxDE_HL_Loop

